#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*byte_str;

	i = 0;
	byte_str = (char *)s;
	while (i < n)
	{
		*(byte_str + i) = 0;
		i++;
	}
}
